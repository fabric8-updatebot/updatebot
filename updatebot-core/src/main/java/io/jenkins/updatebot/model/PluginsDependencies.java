package io.jenkins.updatebot.model;

import io.jenkins.updatebot.support.FileMatcher;

import java.util.ArrayList;
import java.util.List;

/**
 */
public class PluginsDependencies {
    private List<String> includes = new ArrayList<>();
    private List<String> excludes = new ArrayList<>();

    public boolean isEmpty() {
        return includes.isEmpty();
    }

    public List<String> getIncludes() {
        return includes;
    }

    public void setIncludes(List<String> includes) {
        this.includes = includes;
    }

    public List<String> getExcludes() {
        return excludes;
    }

    public void setExcludes(List<String> excludes) {
        this.excludes = excludes;
    }

    public FileMatcher createFileMatcher() {
        return FileMatcher.createFileMatcher(includes, excludes);
    }
}
